// Add the generated sources and jni libraries from UniFFI to the project.
androidComponents {
    onVariants { variant ->
        val generatedSources = "build/generated/source/uniffi/${variant.name}/java"
        if (file(generatedSources).exists()) {
            variant.sources.kotlin?.addStaticSourceDirectory(generatedSources)
            variant.sources.jniLibs?.addStaticSourceDirectory("jniLibs")
        } else {
            logger.warn(
                "UniFFI generated sources not found at $generatedSources. " +
                    "Run `make android-bindings` from the repository root before building."
            )
        }
    }
}

/// Create a `generateUniffiBindingsLibrary` task, which will generate the UniFFI bindings .so libraries
// for Android by running `make android-bindings` from the repository root.
tasks.register<Exec>("generateUniffiBindingsLibrary") {
    group = "build"
    description = "Generates the UniFFI bindings .so libraries for Android."
    commandLine("make", "-C", "${layout.settingsDirectory.asFile}/../..", "android-bindings")
}

// Create `generate{variant}KotlinBindings` tasks for each Android variant, which will generate the Kotlin bindings for that variant
// using the corresponding .so library generated by the `generateUniffiBindingsLibrary` task.
androidComponents {
    onVariants { variant ->
        val validTargetDirNames = listOf("aarch64-linux-android", "armv7-linux-androideabi", "i686-linux-android", "x86_64-linux-android")
        val targetDirWithVariant = runCatching {
            val targetDir = File(layout.settingsDirectory.asFile, "../../target").listFiles().find { it.isDirectory && it.name in validTargetDirNames } ?: return@onVariants
            targetDir.listFiles().find { it.name == "debug" || it.name == "release" } ?: return@onVariants
        }.getOrNull() ?: return@onVariants
        tasks.register<Exec>("generate${variant.name.capitalized()}KotlinBindings") {
            group = "build"
            description = "Generates Kotlin bindings for Android for the ${variant.name} variant."
            mustRunAfter("generateUniffiBindingsLibrary")

            val outDir = "${layout.buildDirectory.get().asFile.path}/generated/source/uniffi/${variant.name}/java"
            commandLine("cargo", "uniffi-bindgen", "generate", "--library", "${targetDirWithVariant}/libuniffi_wysiwyg_composer.so", "--language", "kotlin", "--out-dir", outDir)
        }
    }
}

// Ensure that the UniFFI bindings are generated before publishing to Maven
tasks.withType<AbstractPublishToMaven>().configureEach {
    dependsOn("generateUniffiBindingsLibrary")
    dependsOn("generateReleaseKotlinBindings")
}

// Workaround for https://github.com/mozilla/rust-android-gradle/issues/46
// This looks for the NDK path like: '/some/path/to/sdk/ndk/23.0.7599858'
// and takes the last path component, which should be the version number
fun Project.getNdkVersionAsWorkaround(): String {
    val androidComponents = extensions.getByType(AndroidComponentsExtension::class.java)
    val ndkDirectory = File(androidComponents.sdkComponents.sdkDirectory.get().asFile, "ndk")
    val latestNdk = ndkDirectory.list()?.sorted()?.lastOrNull()
        ?: error("No NDK version found under ${ndkDirectory.absolutePath}")
    println("Using NDK version: $latestNdk")
    return latestNdk
}

